      $stmt->bindParam(':user_id', $user_id, PDO::PARAM_INT);
      $stmt->execute();
      $user = $stmt->fetch(PDO::FETCH_ASSOC);
      
      $user_name = $user ? trim("{$user['fname']} {$user['lname']}") : "User #{$user_id}";

      // Server-side validation
      if (empty($issue)) {
          $notification = "Issue field cannot be empty.";
      } elseif (strlen($issue) < 10) {
          $notification = "Issue must be at least 10 characters long.";
      } else {
          // Check last submission time
          $stmt = $conn->prepare("
              SELECT issue_date 
              FROM issue 
              WHERE user_id = :user_id 
              ORDER BY issue_date DESC 
              LIMIT 1
          ");
          $stmt->bindParam(':user_id', $user_id, PDO::PARAM_INT);
          $stmt->execute();
          $last_issue_date = $stmt->fetchColumn();

          $canSubmit = true;
          if ($last_issue_date) {
              $last_ts = strtotime($last_issue_date);
              $current_ts = strtotime($issue_date);
              if (($current_ts - $last_ts) < 300) {
                  $canSubmit = false;
                  $notification = "You must wait 5 minutes before submitting another issue.";
              }
          }

          if ($canSubmit) {
              // Insert new issue using PDO
              $stmt = $conn->prepare("
                  INSERT INTO issue (user_id, issue, issue_date)
                  VALUES (:user_id, :issue, :issue_date)
              ");
              $stmt->bindParam(':user_id', $user_id, PDO::PARAM_INT);
              $stmt->bindParam(':issue', $issue, PDO::PARAM_STR);
              $stmt->bindParam(':issue_date', $issue_date, PDO::PARAM_STR);
              
              if ($stmt->execute()) {
                  $notification = "Your issue has been placed successfully.";
                  sendSupportMail($user_id, $user_name, $issue, $issue_date);
