/**
 * Send a notification email via SMTP using PHPMailer.
 *
 * @param int    $user_id
 * @param string $user_name
 * @param string $issue
 * @param string $issue_date
 */
function sendSupportMail(int $user_id, string $user_name, string $issue, string $issue_date): void {
    $mail = new PHPMailer(true);
    try {
        // DEBUG: comment out or set to 0 in production
        $mail->SMTPDebug    = 0;
        $mail->Debugoutput  = 'error_log';

        // SMTP server settings
        $mail->isSMTP();
        $mail->Host        = SMTP_HOST;
        $mail->SMTPAuth    = true;
        $mail->Username    = SMTP_USERNAME;
        $mail->Password    = SMTP_PASSWORD;
        $mail->SMTPSecure  = SMTP_SECURE;
        $mail->Port        = SMTP_PORT;

        // Recipients
        $mail->setFrom(SMTP_USERNAME, 'Support System');
        $mail->addAddress(SUPPORT_EMAIL);
        $mail->addBCC(SUPPORT_BCC);

        // Content
        $mail->isHTML(false);
        $mail->Subject = "New Support Issue from {$user_name} (User #{$user_id})";
        $mail->Body    = <<<EOT
You have a new support issue:

User ID:   {$user_id}
Name:      {$user_name}
Date/Time: {$issue_date}
Issue:
{$issue}

-- 
This email was auto‐generated by your support system.
EOT;

        $mail->send();
    } catch (Exception $e) {
        error_log("Support Mailer Error ({$e->getCode()}): " . $mail->ErrorInfo);
    }
}

